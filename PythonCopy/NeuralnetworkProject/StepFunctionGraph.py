#######################################################################################
# 関数名 : StepFunction()
# 返り値 : なし
# 説明　 : ステップ関数
#
#######################################################################################
def StepFunction(x):
    return np.array(x > 0 , dtype = np.int)
    
#######################################################################################
# 関数名 : main()
# 返り値 : なし
# 説明　 : main関数
#
#######################################################################################
def main():
    x = np.arange(-5.0,5.0,0.1)
    y = StepFunction(x)
    plt.plot(x,y)
    plt.ylim(-0.1,1.1)#Y軸の範囲設定
    plt.show(block=False)
    time.sleep(10)
    plt.close()
    return 0

#######################################################################################
if __name__ == "__main__":
        sys.exit(main())
