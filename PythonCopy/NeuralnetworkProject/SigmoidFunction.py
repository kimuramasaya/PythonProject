#######################################################################################
# 関数名 : sigmoid(x)
# 返り値 : return 1/(1+np.exp(-x))
# 説明　 : シグモイド関数
#
#######################################################################################
def sigmoid (x):
    return 1 / ( 1 + np.exp( -x ) )

#######################################################################################
# 関数名 : main()
# 返り値 : なし
# 説明　 : main関数
#
#######################################################################################
def main():
    print(u"シグモイド関数")

    x = np.arange(-5.0,5.0,0.1)
    y = sigmoid(x)
    plt.plot(x,y)
    plt.ylim(-0.1,1.1)
    plt.show(block=False)
    time.sleep(10)
    plt.close()
    return 0

#######################################################################################
if __name__ == "__main__":
        sys.exit(main())
