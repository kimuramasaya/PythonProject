# 説明　 : main関数
#
#######################################################################################
def main():
    x = np.arange(0,10,0.1) #グラフの範囲を設定0～10の0.1ずつ刻む
    y1 = np.sin(x)#sin
    y2 = np.cos(x)#cos

    #グラフに設定
    plt.plot(x,y1,label="sin")
    plt.plot(x,y2,linestyle = "--",label="cos")

    plt.xlabel("x")
    plt.ylabel("y")

    #グラフタイトル
    plt.title("sin & cos")
    plt.legend()

    #描画
    plt.show(block = False) #フラグをFalseにしないと一生閉じない

    time.sleep(10)
    
    plt.close()

    return 0

#######################################################################################
if __name__ == "__main__":
        sys.exit(main())
