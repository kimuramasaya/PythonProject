def main():

    host = "127.29.25.130"
    port = 4000
    count = 0
    sock = socket.socket(socket.AF_INET,socket.SOCK_DGRAM)

    with closing(sock):
        message = 'Hello world : {0}'.format(count).encode('utf-8')
        print(message)
        sock.sendto(message,(host , port))
        count += 1
        time.sleep(0.5)
    
    return 0

#######################################################################################
if __name__ == "__main__":
        sys.exit(main())
