def main():

    host = "127.29.25.130"
    port = 4000
    bufsize = 4096
    
    sock = socket.socket(socket.AF_INET,socket.SOCK_DGRAM)

    with closing(sock):
        sock.bind((host,port))
        while True:
            print(sock.recv(bufsize))
    
    return 0

#######################################################################################
if __name__ == "__main__":
        sys.exit(main())
